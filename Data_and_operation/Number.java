
public class Number {

	public static void main(String[] args) {
		// Operater
		System.out.println(6 + 2); //더하기 8
		System.out.println(6 - 2); //빼기 4
		System.out.println(6*2); //곱하기 12
		System.out.println(6 / 2); //나누기 3

		
		System.out.println(Math.PI); //원주율 3.141592653589793
		System.out.println(Math.floor(Math.PI)); //내림 3.0
		System.out.println(Math.ceil(Math.PI)); //올림 4.0
	}

}
