public class Datatype{
	public static void main(String[] args) {
		System.out.println(6); //숫자열 Number
		System.out.println("six"); //문자열 String
		
		System.out.println("6"); //문자열 String 6
		
		System.out.println(6+6); // 더하기 연산자 12
		System.out.println("6"+"6"); //문자열에서는 결합 연산자 66
		
		System.out.println(6*6); //곱하기 연산자 36
//		System.out.println("6"*"6");
		
		System.out.println("1111".length()); //문자열의 길이를 알려주는 연산
//		System.out.println(1111.length());
	}
}